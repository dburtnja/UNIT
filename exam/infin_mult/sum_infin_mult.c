#include "infin_mult.h"

void	sum_strs(char **strs, int l1)
{
	int		i;
	int		j;

	i = 1;
	while (strs[i] != NULL)
	{
		j = l1;
		while (j > -2)
		{
			strs[0][j] = strs[0][j] + (j + 1 > l1 ? 0 : strs[i][j + 1]);
			j--;
		}

		i++;
	}
}
